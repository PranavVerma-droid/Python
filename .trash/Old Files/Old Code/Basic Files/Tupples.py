#In tupples, it is ordered, enteries cannot be changed and Duplicate enteries can be present

Tupple = ('lion', 'tiger', 'elephant', 'tiger', 2, 2, 3, 4, 6, 8, 8, 5, 9)
print(Tupple)#Prints the entire tupple
print(Tupple[0])#Prints the given index number
print(Tupple.count('tiger')) #counts how many enteries are the same as the given one
print(Tupple.count(2)) #counts how many enteries are the same as the given one